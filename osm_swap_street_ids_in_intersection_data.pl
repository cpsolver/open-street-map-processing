#--------------------------------------------------
#  Input line:
#  0922_1166 w124904767 w133797353 1516989 7699882
#  Output line:
#  0922_1166 w133797353 w124904767 1516989 7699882


while( $input_line = <STDIN> )
{
    chomp( $input_line ) ;
    print $input_line . "\n" ;
    if ( $input_line =~ /([0-9_]+) ([wr][0-9]+) ([wr][0-9]+) (.*)/ )
    {
        print $1 . " " . $3 . " " . $2 . " " . $4 . "\n" ;
    }
}
